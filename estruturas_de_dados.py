# Listas
frutas = ["maçã", "banana", "laranja"]
print("Lista de frutas:", frutas)
frutas.append("uva")
print("Após adicionar 'uva':", frutas)

# Tuplas
ponto = (2, 3)
print("Coordenadas (tupla):", ponto)

# Sets
numeros = {1, 2, 3, 3, 2, 1}
print("Conjunto (sem duplicatas):", numeros)

# Dicionários
aluno = {
    "nome": "João",
    "idade": 21,
    "curso": "Python"
}
print("Nome do aluno:", aluno["nome"])
print("Dados completos:", aluno)
